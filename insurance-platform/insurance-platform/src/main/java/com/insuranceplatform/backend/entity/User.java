package com.insuranceplatform.backend.entity;

import com.insuranceplatform.backend.enums.UserRole;
import com.insuranceplatform.backend.enums.UserStatus;
import jakarta.persistence.*;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import org.hibernate.annotations.CreationTimestamp; // Import for automatic timestamping
import org.hibernate.annotations.UpdateTimestamp;   // Import for automatic timestamping
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.time.LocalDateTime;
import java.util.Collection;
import java.util.List;

@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
@Entity
@Table(name = "users")
public class User implements UserDetails {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false)
    private String fullName;

    @Column(unique = true, nullable = false)
    private String email;

    @Column(unique = true, nullable = false)
    private String phone;

    @Column(nullable = false)
    private String password;

    @Enumerated(EnumType.STRING)
    @Column(nullable = false)
    private UserRole role;

    @Enumerated(EnumType.STRING)
    @Column(nullable = false)
    private UserStatus status;

    // --- NEW FIELDS FOR PASSWORD RESET ---
    private String passwordResetToken;
    private LocalDateTime passwordResetTokenExpiry;
    // --- END OF NEW FIELDS ---

    // --- NEW: Timestamps for Auditing ---
    @CreationTimestamp
    @Column(updatable = false, nullable = false)
    private LocalDateTime createdAt;

    @UpdateTimestamp
    @Column(nullable = false)
    private LocalDateTime updatedAt;
    // --- END OF NEW TIMESTAMPS ---

    // --- Relationship to Agent Profile ---
    @OneToOne(mappedBy = "user", fetch = FetchType.LAZY)
    private Agent agentProfile;

    // --- Placeholders for Advanced Security ---
    @Builder.Default
    private boolean mfaEnabled = false;
    private String mfaSecret; // For TOTP (Google Authenticator)

    // --- UserDetails Methods (Unchanged) ---

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of(new SimpleGrantedAuthority(role.name()));
    }

    @Override
    public String getUsername() {
        return email; // We will use email for login
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return this.status != UserStatus.SUSPENDED;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return this.status == UserStatus.ACTIVE;
    }
}